class loop1{
    public static void main(String[] args){
        System.out.println("printing numbers from 1 to 10");
        for (int i=1;i<=10;i++){
            System.out.print(i+" ");    
        }
        System.out.println("printing even numbers from 1 to 20");
        for(int i=2;i<=20;i=i+2){
            System.out.print(i+" ");
        }    
    }
}








