public class Datatypes1 {
    public static void main(String args[]) {

        int a = 9;
        float b = 10.45676f;
        double c = 34.675;
        char d = 'H';
        boolean e = false;
        byte f = 12;
        long g = 1232245L;
        short h = 1;
        System.out.print("The int datatype is: ");
        System.out.println(a);
        System.out.print("The float datatype is: ");
        System.out.println(b);
        System.out.print("The double datatype is: ");
        System.out.println(c);
        System.out.print("The char datatype is: ");
        System.out.println(d);
        System.out.print("The boolean datatype is: ");
        System.out.println(e);
        System.out.print("The byte datatype is: ");
        System.out.println(f);
        System.out.print("The long datatype is: ");
        System.out.println(g);
        System.out.print("The short datatype is: ");
        System.out.println(h);
        
        
    }
}


